package Programming;

public class PrintUpperCase {

	public static void main(String[] args) {
		String str="Sa4turDay3";
		for(int i=0;i<str.length();i++)
		{
			char ch = str.charAt(i);
			if(ch>='0'&&ch<='9') {
				//System.out.println(ch);
				int a=ch;
				System.out.println(a);
			}
			
		}

	}

}
